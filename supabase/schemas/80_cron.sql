-- cron に関する定義やメモを置くファイルです。
--
-- cron とは、決まった時間や一定間隔で処理を自動実行するための仕組みです。
-- たとえば、毎朝の通知、期限切れチェック、日次集計、定期的な同期処理などで使います。
--
-- 業務アプリでは後から必要になることが多いため、このテンプレートでは
-- `public` だけでなく `cron` も管理対象に含めています。
--
-- まだ定期実行が不要なプロジェクトでは、このファイルはコメントだけのままで構いません。
-- 必要になったタイミングで、AI や公式ドキュメントを使いながら定義を追加してください。
--
-- 例:
-- - 毎日 09:00 に在庫チェックを実行する
-- - 毎週月曜に未対応タスクの集計を実行する
-- - 定期的に Edge Functions を呼び出してメール通知を行う
--
-- 注意:
-- - 定期実行の定義は Supabase Studio だけで完結させず、必ずこの `schemas` 配下で管理してください。
-- - 変更後は `npm run db:diff <migration名>` で migration を生成し、`supabase/migrations/` も一緒にコミットしてください。
